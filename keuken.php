In de keuken van <i>De Pennelekker</i> zwaait de patron de plak. 
U moet er hem verplicht bij nemen bij de huur van een zaal; maar geen zorgen, in zijn keuken is Guido als een vis in een bouillabai... euh, in het water! 
U kunt er van op aan dat hij uw gasten verwent van amuse tot dessert.
<br><br>
In <i>De Pennelekker</i> krijgt u een moderne Franse keuken geserveerd. 
Samen met zijn keukenteam verzorgt Guido graag uw receptie, walking dinner, babyborrel, koud of warm buffet en eender welk feestmenu. 
<br><br>
We hebben een uitgebreide menukaart waarin u zeker voor elke gang iets naar uw smaak kunt vinden. 
Zit uw lievelingskostje er toch niet bij? Geen probleem, Guido staat open voor uw suggesties.
