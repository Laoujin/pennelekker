Voor een etentje in kleine kring hoeft u bij ons geen zaal te huren. Daarvoor hebben wij een restaurantformule. Die is bedoeld voor etentjes van 10 tot 20 personen. Reserveren kunt u ten laatste een week op voorhand. U kiest een menu voor het hele tafelgezelschap; we bedienen niet à la carte.
<br><br>
Enkele keren per jaar toveren we <i>De Pennelekker</i> om van feestzaal tot restaurant. Guido bekokstooft dan een degustatiemenu waarin hij zich helemaal uitleeft. Wanneer? Bijvoorbeeld op Valentijnsdag, met Pasen, met Moeder- of Vaderdag, in de herfst en tijdens de eindejaarsperiode.
<br><br>
Het degustatiemenu kunt u komen eten met z'n tweetjes, of zelfs met 30. Zoals u wilt. Het enige wat u moet doen, is op voorhand reserveren. U kunt het <a href=menus.php>menu bekijken</a> op deze website. Of <a href='mailto:<?=MAIL?>'>stuur Guido een mail</a> met het verzoek in de adreslijst opgenomen te worden: u krijgt het menu dan in uw mailbox en/of brievenbus.

