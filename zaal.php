U heeft iets te vieren? Dan kunt u bij ons terecht met een groep van 20 tot 200 personen.
<br><br>
We stemmen de zaal af op uw aantal gasten, via een flexibel houten wandsysteem.
Kleine groepen kiezen uit twee kleinere zaaltjes. Zo blijft het altijd gezellig.
<br><br>
Voor de feestneuzen hebben we een dansvloer ter beschikking. 
De airco zorgt ervoor dat u stevig door kunt dansen. 
Dat u toch niet lichtjes gaat zweten wanneer die ene tante of nonkel eraan komt, kunnen we echter niet verzekeren!
<br><br>
U kent uw gasten beter dan wij. Da's nogal wiedes. 
We bespreken dan ook graag uw plannen voor de tafelschikking. 
Zo wordt uw feest echt een feest voor iedereen. 
Feestelijk wordt het trouwens sowieso. 
We dekken onze tafels met fris linnen; verse bloemen en kaarsjes maken het af. 
Wilt u zelf nog voor extra versiering zorgen? Dat kan.
<br><br>
Nog vragen? Neem vrijblijvend contact met ons op.

